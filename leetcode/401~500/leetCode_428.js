class Codec {
  constructor() {}

  /**
   * @param {Node|null} root
   * @return {string}
   */
  // Encodes a tree to a single string.
  serialize = function (root) {
    return root;
  };

  /**
   * @param {string} data
   * @return {Node|null}
   */
  // Decodes your encoded data to tree.
  deserialize = function (data) {
    return data;
  };
}
